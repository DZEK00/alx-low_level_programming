/**
 * mul - function that prints the product of
 * two integers
 *
 * @a: Takes integer type as input of the function
 *
 * @b: Takes integer type as input of the function
 *
 * Return: The result of multiplying two integers
 */

int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}

