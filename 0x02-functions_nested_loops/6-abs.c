/**
 * _abs - function that computes the absolute value
 * of an integer
 *
 * @n: takes in integer type input
 *
 * return - -n if the integer is less than 0 and
 * n if the integer is greater than 0
 */

int _abs(int n)
{
	if (n < 0)
	{
		return (-n);
	}
	else
	{
		return (n);
	}
		return (0);
}
