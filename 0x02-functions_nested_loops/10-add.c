/**
 * add - function that prints the sum of
 * two integers
 *
 * @x: Takes integer type as input of the function
 *
 * @y: Takes integer type as input of the function
 *
 * Return: The result of adding two integers
 */

int add(int x, int y)
{
	int result;

	result = x + y;
	return (result);
}
